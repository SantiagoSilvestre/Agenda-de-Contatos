    private void btn_SalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SalvarActionPerformed
        // TODO add your handling code here:
        try{
            String cod = tf_Codigo.getText();
            String nome = tf_Nome.getText();
            String email = tf_Email.getText();
            String telefone = tf_Telefone.getText();
            String foto = tf_Foto.getText();
            if (foto.isEmpty()) {
                foto = "default.jpg";
            }
            if (cod.isEmpty()) {
                //Inserção no Banco de Dados
                String insertSQL = "Insert into Contatos (nome, email, telefone,"+
                "foto) values ('"+nome+"','"+email+"',"+
                "'"+telefone+"','"+foto+"')";
                con_agenda.statement.executeUpdate(insertSQL);
            } else {
                // Atualização do Banco 
                String updateSQL = "Update Contatos set "
                        + "nome = '"+nome+"',"
                        + "email = '"+email+"',"
                        + "telefone = '"+telefone+"',"
                        + "foto = '"+foto+"' where codigo = '"+cod+"' ";
                System.out.println(updateSQL);
                con_agenda.statement.execute(updateSQL);
            }
            JOptionPane.showMessageDialog(null, "Salvo Com Sucesso!!!");
            con_agenda.executeSQL("select * from Contatos order by codigo asc");
            con_agenda.resultset.first();
            mostrar_Dados();

        }
        catch(SQLException erro){
            JOptionPane.showMessageDialog(null, "Erro ao Gravar");

        }
        
        
        
    }//GEN-LAST:event_btn_SalvarActionPerformed

