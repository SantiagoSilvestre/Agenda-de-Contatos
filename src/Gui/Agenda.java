    private void btn_DeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_DeletarActionPerformed
        // TODO add your handling code here:
        try{
            String sql ="select * from Contatos where codigo = "+tf_Codigo.getText();
            con_agenda.executeSQL(sql);
            con_agenda.resultset.first();
            String nome ="Deletar o Contato : "+con_agenda.resultset.getString("nome");
            int opcao_escolhida = JOptionPane.showConfirmDialog(null, nome,"Remover"+
                    "Contatos", JOptionPane.YES_OPTION);
            if (opcao_escolhida == JOptionPane.YES_OPTION){
                
                sql = "delete  from Contatos where codigo = "+tf_Codigo.getText();
                
                int conseguiu_excluir = con_agenda.statement.executeUpdate(sql);
                
                if(conseguiu_excluir ==1){
                    JOptionPane.showMessageDialog(null,"Excluido com Sucesso!!!");
                    //atualiza o resultset
                    con_agenda.executeSQL("select * from Contatos");
                    
                    //posiciona no primeiro registro
                    con_agenda.resultset.first();
                    mostrar_Dados();
                }
            } else 
                return ;
        }
        catch(SQLException erro){
            JOptionPane.showMessageDialog(null,"Erro ao Excluir!\n"+ erro);
        }
    }//GEN-LAST:event_btn_DeletarActionPerformed

