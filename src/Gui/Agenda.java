        tf_Codigo.setText(con_agenda.resultset.getString("codigo"));
        lb_Ft.setText("");
        lb_Ft.setIcon(new javax.swing.ImageIcon(getClass().getResource("/Imagens/"+tf_Foto.getText())));
        
